# Linked list is a form of a sequence collection and it does not have to be in orer.
# A linked list is made up of nodes that contain data and a reference to the next node.

# Types of Linked List:
# 1. Singly Linked List: Each node points to the next node and the last node points to None.
# 2. Doubly Linked List: Each node points to both the next and previous nodes.
# 3. Circular Linked List: The last node points back to the first node, forming a circle.   
# 4. Circular Doubly Linked List: Combines the features of both circular and doubly linked lists.